public class Fish {
    private String name;
    private int pointGain;

    public Fish (String name, int pointGain)
    {
        this.name = name;
        this.pointGain = pointGain;
    }
    public String getName()
    {
        return name;
    }

    public int getPointGain()
    {
        return pointGain;
    }

}
